# Few commonly used dictonary methods.

dict = {
    "good": "acha",
    "bad" :"bura",
    "naughty" : "badmash",
    "cheerful" : "umang se bhara hua"
}

print(dict.items())
print(dict.keys())
print(dict.values())
print(dict.get("bad"))
print(dict.update({"bad":"kharab"}))
print(dict.update({"red":"laal"}))
print(dict)

